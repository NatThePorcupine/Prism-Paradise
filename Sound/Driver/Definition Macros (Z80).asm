; =========================================================================================================================================================
; Driver Definition Macros (Z80-Side)
; =========================================================================================================================================================
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Reserve symbol macros
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
__rs := 0

rsset	macro	value
__rs := value
	endm

rs	macro	{INTLABEL}, value
__LABEL__ label __rs
__rs := __rs + value
	endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Define the space occupied by a music entry
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
music_entry	macro	{INTLABEL}, addr, flag
__LABEL__	rs	1
		ds	3
		endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Define the space occupied by a pcm id entry
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
pcm_id_entry	macro	id, pitch
		ds	2
		endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Define the space occupied by a pcm pointer entry
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
pcm_ptr_entry	macro	{INTLABEL}, addr
__LABEL__	rs	1
		ds	2
		endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Define the space occupied by an id entry
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
id_entry	macro	id
		ds	1
		endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Define the space occupied by a bank entry
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
bank_entry	macro	addr
		ds	1
		endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
; Define the space occupied by an address entry
; ---------------------------------------------------------------------------------------------------------------------------------------------------------
address_entry	macro	addr
		ds	2
		endm
; ---------------------------------------------------------------------------------------------------------------------------------------------------------